export const getCategoryIntegrations = (category: string): EnterpriseIntegration[] => {
  return enterpriseIntegrations.filter((integration) => integration.category === category);
};

export const getConnectedIntegrations = (): EnterpriseIntegration[] => {
  return enterpriseIntegrations.filter((integration) => integration.connected);
};

export const getAllCategories = (): string[] => {
  return Array.from(new Set(enterpriseIntegrations.map((integration) => integration.category)));
};
